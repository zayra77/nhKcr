			<h1 class="title">Help for using nhKCR</h1>
			<p class="byline"><small>Input:</small></p>
			<div class="entry">
				<p>The protein sequence input to our server should be FASTA format. Only the 20 conventional amino acid symbols are supported. A maximum of 100 protein sequence are supported by nhKcr at a time.</p>
			</div>
			
			<p class="byline"><small>Output:</small></p>
			<div class="entry">
			<p>The prediction output consists of four items:</p>
			<ul>
				<li>Protein name_Position</li>
				<li>Peptide</li>
				<li>Score</li>
				<li>Prediction (YES for Crotonylation sites, NO for non-crotonylation sites). </li>				
			</ul>		
			</div>			
			
			<p class="byline"><small>Query your result:</small></p>
			<div class="entry">
			<p>You can query your prediction result in the job list page or input you job ID in the query form.</p>
			</div>			
